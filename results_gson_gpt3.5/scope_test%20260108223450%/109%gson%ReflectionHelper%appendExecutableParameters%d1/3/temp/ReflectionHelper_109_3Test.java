class ReflectionHelperAppendExecutableParametersTest {

  // Sample classes for testing

  static class NoParamsConstructorClass {
    public NoParamsConstructorClass() {}
  }

  static class OneParamConstructorClass {
    public OneParamConstructorClass(String s) {}
  }

  static class MultiParamConstructorClass {
    public MultiParamConstructorClass(int i, double d, String s) {}
  }

  static class NoParamsMethodClass {
    public void noParamsMethod() {}
  }

  static class OneParamMethodClass {
    public void oneParamMethod(String s) {}
  }

  static class MultiParamMethodClass {
    public void multiParamMethod(int i, double d, String s) {}
  }

  @Test
    @Timeout(8000)
  void testAppendExecutableParameters_withNoParamsConstructor() throws Exception {
    Constructor<?> constructor = NoParamsConstructorClass.class.getConstructor();
    StringBuilder sb = new StringBuilder();

    invokeAppendExecutableParameters(constructor, sb);

    assertEquals("()", sb.toString());
  }

  @Test
    @Timeout(8000)
  void testAppendExecutableParameters_withOneParamConstructor() throws Exception {
    Constructor<?> constructor = OneParamConstructorClass.class.getConstructor(String.class);
    StringBuilder sb = new StringBuilder();

    invokeAppendExecutableParameters(constructor, sb);

    assertEquals("(String)", sb.toString());
  }

  @Test
    @Timeout(8000)
  void testAppendExecutableParameters_withMultiParamConstructor() throws Exception {
    Constructor<?> constructor = MultiParamConstructorClass.class.getConstructor(int.class, double.class, String.class);
    StringBuilder sb = new StringBuilder();

    invokeAppendExecutableParameters(constructor, sb);

    assertEquals("(int, double, String)", sb.toString());
  }

  @Test
    @Timeout(8000)
  void testAppendExecutableParameters_withNoParamsMethod() throws Exception {
    Method method = NoParamsMethodClass.class.getMethod("noParamsMethod");
    StringBuilder sb = new StringBuilder();

    invokeAppendExecutableParameters(method, sb);

    assertEquals("()", sb.toString());
  }

  @Test
    @Timeout(8000)
  void testAppendExecutableParameters_withOneParamMethod() throws Exception {
    Method method = OneParamMethodClass.class.getMethod("oneParamMethod", String.class);
    StringBuilder sb = new StringBuilder();

    invokeAppendExecutableParameters(method, sb);

    assertEquals("(String)", sb.toString());
  }

  @Test
    @Timeout(8000)
  void testAppendExecutableParameters_withMultiParamMethod() throws Exception {
    Method method = MultiParamMethodClass.class.getMethod("multiParamMethod", int.class, double.class, String.class);
    StringBuilder sb = new StringBuilder();

    invokeAppendExecutableParameters(method, sb);

    assertEquals("(int, double, String)", sb.toString());
  }

  private void invokeAppendExecutableParameters(AccessibleObject executable, StringBuilder sb) throws Exception {
    Method method = ReflectionHelper.class.getDeclaredMethod("appendExecutableParameters", AccessibleObject.class, StringBuilder.class);
    method.setAccessible(true);
    method.invoke(null, executable, sb);
  }
}