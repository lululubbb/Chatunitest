  // We cannot change the package of these classes, so we create a utility method to get a Class object
  // with a fake getName() by using a proxy ClassLoader and define a dummy class with the desired name.

  private static Class<?> createFakeClassWithName(String fakeName) {
    // Create a dummy class with the desired name by defining a class dynamically with the given name
    // Using an in-memory class loader
    try {
      final String className = fakeName;
      byte[] classBytes = generateEmptyClassBytes(className);
      ClassLoader cl = new ClassLoader(ReflectionAccessFilterHelperTest.class.getClassLoader()) {
        @Override
        protected Class<?> findClass(String name) throws ClassNotFoundException {
          if (name.equals(className)) {
            return defineClass(name, classBytes, 0, classBytes.length);
          }
          return super.findClass(name);
        }
      };
      return cl.loadClass(className);
    } catch (Exception e) {
      throw new RuntimeException(e);
    }
  }

  private static byte[] generateEmptyClassBytes(String className) {
    // Generate a minimal valid class bytecode with the specified className.
    // className is fully qualified with dots, need to convert to slashes
    String internalName = className.replace('.', '/');
    // Minimal class bytecode generated by ASM or hand-crafted
    // Here, hardcode a minimal class bytecode for the class:
    // public class <className> {}
    // Using ASM is easier, but not allowed here, so use a prebuilt byte array and patch the name.

    // Below is a minimal class bytecode for a public class with no members:
    byte[] baseClass = new byte[] {
      (byte)0xCA, (byte)0xFE, (byte)0xBA, (byte)0xBE, // magic
      0x00, 0x00, 0x00, 0x34, // version 52 (Java 8)
      0x00, 0x0A, // constant pool count = 10
      0x07, 0x00, 0x02, // #1 = Class #2
      0x01, 0x00, 0x10, // #2 = Utf8 "java/lang/Object"
      0x07, 0x00, 0x04, // #3 = Class #4
      0x01, 0x00, 0x0F, // #4 = Utf8 "FakeClassName"
      0x01, 0x00, 0x06, // #5 = Utf8 "<init>"
      0x01, 0x00, 0x03, // #6 = Utf8 "()V"
      0x01, 0x00, 0x04, // #7 = Utf8 "Code"
      0x0C, 0x00, 0x05, 0x00, 0x06, // #8 = NameAndType #5:#6
      0x0A, 0x00, 0x01, 0x00, 0x08, // #9 = Methodref #1.#8
      0x00, 0x21, // access flags: public super
      0x00, 0x03, // this class #3
      0x00, 0x01, // super class #1
      0x00, 0x00, // interfaces count
      0x00, 0x00, // fields count
      0x00, 0x01, // methods count
      0x00, 0x01, // method access flags: public
      0x00, 0x05, // name index #5 <init>
      0x00, 0x06, // descriptor index #6 ()V
      0x00, 0x01, // attributes count
      0x00, 0x07, // attribute name index #7 Code
      0x00, 0x00, 0x00, 0x11, // attribute length 17
      0x00, 0x01, // max stack
      0x00, 0x01, // max locals
      0x00, 0x05, // code length
      0x2A, // aload_0
      (byte)0xB7, 0x00, 0x09, // invokespecial #9
      (byte)0xB1, // return
      0x00, 0x00, // exception table length
      0x00, 0x00, // attributes count
      0x00, 0x00 // end
    };

    // Patch the Utf8 constant #4 "FakeClassName" with the correct internalName
    // The Utf8 length is 15 (0x0F), we need to replace "FakeClassName" with internalName and pad if shorter

    byte[] nameBytes = internalName.getBytes();
    if (nameBytes.length > 15) {
      throw new IllegalArgumentException("Class name too long for this minimal class generator");
    }

    baseClass[19] = (byte) nameBytes.length; // length byte for Utf8

    System.arraycopy(nameBytes, 0, baseClass, 20, nameBytes.length);

    // If shorter than 15, pad with zeros
    for (int i = 20 + nameBytes.length; i < 20 + 15; i++) {
      baseClass[i] = 0;
    }

    return baseClass;
  }

