class CSVFormatPrintTest {

    private CSVFormat csvFormat;

    @BeforeEach
    void setUp() {
        csvFormat = CSVFormat.DEFAULT;
    }

    @Test
    @Timeout(8000)
    void testPrintReturnsCSVPrinter() throws IOException {
        Appendable appendable = mock(Appendable.class);
        CSVPrinter printer = csvFormat.print(appendable);
        assertNotNull(printer);

        // Access private fields from CSVPrinter itself or its superclass via reflection
        Object outField = getField(printer, "out");
        Object formatField = getField(printer, "format");

        assertSame(appendable, outField);
        assertSame(csvFormat, formatField);
    }

    // Utility method to access private fields via reflection, including superclass fields
    private Object getField(Object obj, String fieldName) {
        Class<?> clazz = obj.getClass();
        while (clazz != null) {
            try {
                Field field = clazz.getDeclaredField(fieldName);
                field.setAccessible(true);
                return field.get(obj);
            } catch (NoSuchFieldException e) {
                clazz = clazz.getSuperclass();
            } catch (Exception e) {
                fail("Reflection error: " + e.getMessage());
                return null;
            }
        }
        fail("Field '" + fieldName + "' not found in class hierarchy.");
        return null;
    }
}