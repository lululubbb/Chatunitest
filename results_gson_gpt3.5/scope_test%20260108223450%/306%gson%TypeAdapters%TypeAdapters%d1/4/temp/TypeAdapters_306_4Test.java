  public void testConstructor_throwsUnsupportedOperationException() throws Exception {
    Constructor<TypeAdapters> constructor = TypeAdapters.class.getDeclaredConstructor();
    constructor.setAccessible(true);
    InvocationTargetException thrown = assertThrows(InvocationTargetException.class, () -> {
      constructor.newInstance();
    });
    // Check that the cause is UnsupportedOperationException
    assertThrows(UnsupportedOperationException.class, () -> {
      throw thrown.getCause();
    });
  }
